/* Một dãy các số nguyên không âm A[1], A[2], ..., A[N] được gọi là dãy tổng 
đối xứng nếu ta có thể tách dãy đó làm 2 dãy có tổng các giá trị bằng nhau. Nghĩa là 
tồn tại một số k trong đoạn [1..N] sao cho tổng                   A[1] + A[2] + ... + A[k] 
= A[k+1] + A[k+2] + ... + A[N]. 
* Yêu cầu: Cho một dãy gồm N số nguyên không âm. Hãy tìm dãy con gồm các 
phần tử liên tiếp dài nhất mà cũng là dãy tổng đối xứng.  
* Dữ liệu vào: Từ tệp văn bản BAI1.INP  có cấu trúc: 
      - Dòng đầu tiên chứa số nguyên N (2 ≤ N≤5000). 
      - N dòng tiếp theo, dòng thứ i trong N dòng chứa giá trị của phần tử A[i] của dãy 
( i = 1, 2, ..., N). Các số trong dãy không âm và nhỏ hơn 200000 
* Dữ liệu ra: Ghi vào tệp văn bản BAI1.OUT  có cấu trúc: 
       Gồm một dòng: Ghi 1 số là độ dài lớn nhất của dãy con gồm các phần tử liên 
tiếp dài nhất là dãy tổng đối xứng.  Nếu không có kết quả thì ghi số 0. */
